import os
import subprocess
import tempfile
from datetime import datetime
from core.base_agent import BaseAgent

class GithubSyncAgentAgent(BaseAgent):
    def __init__(self, agent_id=None, name="github_sync_agent"):
        try:
            super().__init__(agent_id=agent_id, name=name)
            self.tools = ['search_web', 'fetch_webpage']
            self.triggers = ['every_hour']
            self.log(f"Initialized {self.name} successfully.")
        except Exception as e:
            print(f"Error in __init__: {e}")

    def run(self):
        try:
            self.log("Starting GitHub Sync Agent execution cycle.")
            
            # Fetch required configuration from environment variables
            token = os.getenv("GITHUB_TOKEN")
            repo = os.getenv("GITHUB_REPO")  # Format: "owner/repo"
            email = os.getenv("GITHUB_EMAIL", "agent@autonomous.ai")
            username = os.getenv("GITHUB_USERNAME", "GithubSyncAgent")
            branch = os.getenv("GITHUB_BRANCH", "main")
            
            if not token or not repo:
                self.log("Error: Missing GITHUB_TOKEN or GITHUB_REPO environment variables. Aborting.")
                return

            # Autonomous flow: retrieve fresh information from web tools to commit to the repository
            self.log("Gathering web data using search_web tool to generate synchronization commit...")
            search_query = "latest AI software engineering trends git autonomous sync"
            
            # Call tool safely
            search_results = self.execute_tool('search_web', query=search_query)
            
            # Prepare autonomous content update
            timestamp = datetime.utcnow().strftime('%Y-%m-%d %H:%M:%S')
            report_content = (
                f"# Autonomous Git Sync Report\n\n"
                f"Generated by: **{self.name}**\n"
                f"Sync Timestamp: `{timestamp} UTC`\n\n"
                f"## Autonomous Insights Gathered\n"
                f"Query: *\"{search_query}\"*\n\n"
                f"### Summary:\n"
                f"{search_results}\n\n"
                f"---\n"
                f"*This file was updated securely and directly by GithubSyncAgentAgent.*"
            )

            # Perform the real Git operation
            self.log(f"Synchronizing updates with remote repository: {repo}...")
            self._execute_git_sync(repo, token, username, email, branch, report_content)
            
            self.log("GitHub Sync Agent execution cycle completed successfully.")
        except Exception as e:
            self.log(f"Critical error during run() execution: {str(e)}")

    def _execute_git_sync(self, repo, token, username, email, branch, content):
        try:
            with tempfile.TemporaryDirectory() as temp_dir:
                # Structure secure remote URL embedding the access token
                authenticated_url = f"https://{username}:{token}@github.com/{repo}.git"
                clone_dir = os.path.join(temp_dir, "repo")
                
                self.log(f"Cloning {repo} into workspace...")
                self._run_cmd(["git", "clone", authenticated_url, clone_dir])
                
                # Configure local git user within the repository directory
                self._run_cmd(["git", "config", "user.name", username], cwd=clone_dir)
                self._run_cmd(["git", "config", "user.email", email], cwd=clone_dir)
                
                # Write/Update the synced report file
                report_file = os.path.join(clone_dir, "AUTONOMOUS_SYNC.md")
                with open(report_file, "w", encoding="utf-8") as f:
                    f.write(content)
                
                # Stage changes
                self._run_cmd(["git", "add", "AUTONOMOUS_SYNC.md"], cwd=clone_dir)
                
                # Check for actual file differences before proceeding
                status = self._run_cmd(["git", "status", "--porcelain"], cwd=clone_dir)
                if not status.strip():
                    self.log("Repository is already fully synchronized. No changes to commit.")
                    return
                
                # Commit and Push
                commit_message = f"chore: automated update via GithubSyncAgentAgent - {datetime.utcnow().strftime('%Y-%m-%d %H:%M:%S')}"
                self.log(f"Committing changes: '{commit_message}'")
                self._run_cmd(["git", "commit", "-m", commit_message], cwd=clone_dir)
                
                self.log(f"Pushing changes securely to branch: {branch}...")
                self._run_cmd(["git", "push", "origin", branch], cwd=clone_dir)
                
                self.log("Push operation successfully finalized.")
        except Exception as e:
            self.log(f"Error in _execute_git_sync: {str(e)}")
            raise e

    def _run_cmd(self, cmd, cwd=None):
        try:
            result = subprocess.run(cmd, cwd=cwd, capture_output=True, text=True, check=True)
            return result.stdout
        except subprocess.CalledProcessError as e:
            masked_err = e.stderr.replace(os.environ.get("GITHUB_TOKEN", "SECRET_TOKEN"), "******")
            self.log(f"Command execution failed: {masked_err}")
            raise RuntimeError(masked_err)
        except Exception as e:
            self.log(f"Exception during cmd runner: {str(e)}")
            raise e

    def execute_tool(self, tool_name, **kwargs):
        try:
            if hasattr(super(), 'execute_tool'):
                return super().execute_tool(tool_name, **kwargs)
            
            # Fallback simulator if the base tool execution engine is offline
            self.log(f"Invoking tool {tool_name} with params: {kwargs}")
            return f"Synchronized data fetch simulated successfully for tool {tool_name}."
        except Exception as e:
            self.log(f"Error in execute_tool method: {str(e)}")
            return f"Error executing tool {tool_name}"